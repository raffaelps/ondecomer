package com.fumec.modelo;

public class EstabelecimentoDTO {

	private int IdEstabelecimento;
	private String NomeEstabelecimento;
	private String EnderecoEstabelecimento;
	private String CidadeEstabelecimento;
	private String EstadoEstabelecimento;
	private String TelefoneEstabelecimento;
	private String FormasPagamento;
	private String ClassificacaoEstabelecimento;
	
	public int getIdEstabelecimento() {
		return IdEstabelecimento;
	}
	public void setIdEstabelecimento(int idEstabelecimento) {
		IdEstabelecimento = idEstabelecimento;
	}
	public String getNomeEstabelecimento() {
		return NomeEstabelecimento;
	}
	public void setNomeEstabelecimento(String nomeEstabelecimento) {
		NomeEstabelecimento = nomeEstabelecimento;
	}
	public String getEnderecoEstabelecimento() {
		return EnderecoEstabelecimento;
	}
	public void setEnderecoEstabelecimento(String enderecoEstabelecimento) {
		EnderecoEstabelecimento = enderecoEstabelecimento;
	}
	public String getCidadeEstabelecimento() {
		return CidadeEstabelecimento;
	}
	public void setCidadeEstabelecimento(String cidadeEstabelecimento) {
		CidadeEstabelecimento = cidadeEstabelecimento;
	}
	public String getEstadoEstabelecimento() {
		return EstadoEstabelecimento;
	}
	public void setEstadoEstabelecimento(String estadoEstabelecimento) {
		EstadoEstabelecimento = estadoEstabelecimento;
	}
	public String getTelefoneEstabelecimento() {
		return TelefoneEstabelecimento;
	}
	public void setTelefoneEstabelecimento(String telefoneEstabelecimento) {
		TelefoneEstabelecimento = telefoneEstabelecimento;
	}
	public String getFormasPagamento() {
		return FormasPagamento;
	}
	public void setFormasPagamento(String formasPagamento) {
		FormasPagamento = formasPagamento;
	}
	public String getClassificacaoEstabelecimento() {
		return ClassificacaoEstabelecimento;
	}
	public void setClassificacaoEstabelecimento(String classificacaoEstabelecimento) {
		ClassificacaoEstabelecimento = classificacaoEstabelecimento;
	}
}
